<script lang="ts">
import {
  Component,
  Emit,
  Vue,
  Prop,
} from 'vue-property-decorator';

@Component
export default class Piece extends Vue {
  @Prop() readonly cell!: {
    element: string
    frozen: boolean,
    x: number,
    y: number,
    rotation: number,
  }

  // for toolbox only:
  @Prop() readonly disabled!: boolean

  onDragEnd(e) {
    // console.log(e.dataTransfer.getData('Tile'));
    this.$emit('onDragEnd', this.cell)
  }

  mounted() {
    // console.log(this.cell);
  }

  get isDraggable(): boolean {
    return !this.cell.frozen && !this.disabled;
  }


  @Emit('pieceDragStart')
  onPieceDragStart(e: DragEvent) {
    const dt = e.dataTransfer;

    const {
      element,
      frozen,
      x,
      y,
      rotation,
    } = this.cell;

    // no dragging, in case there is text present
    if (frozen) {
      if (dt) {
        dt.clearData();
      }
      return false;
    }

    if (dt) {
      dt.setData('text/plain', element);
      dt.setData('originY', String(y));
      dt.setData('originX', String(x));
      dt.setData('rotation', String(rotation));
    }
    return this.cell;
  }
}


</script>

